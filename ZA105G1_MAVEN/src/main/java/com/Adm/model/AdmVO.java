package com.Adm.model;


public class AdmVO implements java.io.Serializable{
	private Integer admin_no;
	private String admin_account;
	private String admin_password;
	private String admin_name;
	private String admin_email;
	private String admin_phone;
	private String admin_address;
	
	public Integer getAdmin_no() {
		return admin_no;
	}
	public void setAdmin_no(Integer admin_no) {
		this.admin_no = admin_no;
	}
	public String getAdmin_account() {
		return admin_account;
	}
	public void setAdmin_account(String admin_account) {
		this.admin_account = admin_account;
	}
	public String getAdmin_password() {
		return admin_password;
	}
	public void setAdmin_password(String admin_password) {
		this.admin_password = admin_password;
	}
	public String getAdmin_name() {
		return admin_name;
	}
	public void setAdmin_name(String admin_name) {
		this.admin_name = admin_name;
	}
	public String getAdmin_email() {
		return admin_email;
	}
	public void setAdmin_email(String admin_email) {
		this.admin_email = admin_email;
	}
	public String getAdmin_phone() {
		return admin_phone;
	}
	public void setAdmin_phone(String admin_phone) {
		this.admin_phone = admin_phone;
	}
	public String getAdmin_address() {
		return admin_address;
	}
	public void setAdmin_address(String admin_address) {
		this.admin_address = admin_address;
	}

	
}
