package com.friend.model;

public class FriendVO implements java.io.Serializable{
	private Integer mem_no;
	private Integer friend_no;
	private Integer friend_status;
	
	public Integer getMem_no() {
		return mem_no;
	}
	public void setMem_no(Integer mem_no) {
		this.mem_no = mem_no;
	}
	public Integer getFriend_no() {
		return friend_no;
	}
	public void setFriend_no(Integer friend_no) {
		this.friend_no = friend_no;
	}
	public Integer getFriend_status() {
		return friend_status;
	}
	public void setFriend_status(Integer friend_status) {
		this.friend_status = friend_status;
	}
	  
	
}
