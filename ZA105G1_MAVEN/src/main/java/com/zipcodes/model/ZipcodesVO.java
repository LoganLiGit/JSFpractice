package com.zipcodes.model;

public class ZipcodesVO {
	private Integer zipcodes_no;
	private String zipcodes_district;
	private String zipcodes_city; 
	
	public Integer getZipcodes_no() {
		return zipcodes_no;
	}
	public void setZipcodes_no(Integer zipcodes_no) {
		this.zipcodes_no = zipcodes_no;
	}
	public String getZipcodes_district() {
		return zipcodes_district;
	}
	public void setZipcodes_district(String zipcodes_district) {
		this.zipcodes_district = zipcodes_district;
	}
	public String getZipcodes_city() {
		return zipcodes_city;
	}
	public void setZipcodes_city(String zipcodes_city) {
		this.zipcodes_city = zipcodes_city;
	}
}
